-- Retrieve Average Salary by Gender and Industry:
SELECT Industry, Gender, AVG(TotalCompensation) AS AvgSalary
FROM employee e
JOIN Salaries s ON e.EmployeeID = s.EmployeeID
GROUP BY Industry, Gender;

-- Identify Gender Wage Gap by Job Role:
SELECT JobRole, Gender, AVG(TotalCompensation) AS AvgSalary
FROM Employee e
JOIN Salaries s ON e.EmployeeID = s.EmployeeID
GROUP BY JobRole, Gender;

-- Analyze Performance Scores by Gender:
SELECT Gender, AVG(PerformanceRating) AS AvgPerformance
FROM Employee e
JOIN Performance p ON e.EmployeeID = p.EmployeeID
GROUP BY Gender;









