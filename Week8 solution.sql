CREATE DATABASE Equality_sal; -- creating Equality_sal database based on Brightstart ERD model
use equality_sal;

-- Creating tables 
CREATE TABLE Education (
    EducationLevel VARCHAR(50),
    EducationID INT ,
    Industry VARCHAR(50)
);

CREATE TABLE Employee (
    EmployeeID INT,
    Name VARCHAR(100),
    Gender VARCHAR(10),
    Age INT,
    EducationLevel VARCHAR(50),
    Industry VARCHAR(50),
    JobRole VARCHAR(50)
        );
        
CREATE TABLE Performance (
    EmployeeID INT,
    Year INT,
    PerformanceRating INT,
    PerformanceReviewComments TEXT
    );

CREATE TABLE Salaries (
    SalaryID INT PRIMARY KEY,
    EmployeeID INT,
    BaseSalary DECIMAL(10, 2),
    Bonus DECIMAL(10, 2),
    TotalCompensation DECIMAL(10, 2),
    Year INT
      );
      
